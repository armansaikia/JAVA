package JAVA.Variables.Datatypes;

public class booleanexample {
    public static void main(String[] args) {
        boolean isRaining = true; // Declare a boolean variable

        if (isRaining) {
            System.out.println("It is raining. Don't forget your umbrella!");
        } else {
            System.out.println("No rain today. Enjoy the sunshine!");
        }
    }
}

